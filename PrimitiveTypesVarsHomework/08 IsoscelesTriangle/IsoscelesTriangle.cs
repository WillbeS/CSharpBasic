using System;

    class IsoscelesTriangle
    {
        static void Main()
        {
            char copyrightSymbol = '\u00A9';

            Console.WriteLine("   " + copyrightSymbol);
            Console.WriteLine("  " + copyrightSymbol + " " + copyrightSymbol);
            Console.WriteLine(" " + copyrightSymbol + "   " + copyrightSymbol);
            Console.WriteLine(copyrightSymbol + " " + copyrightSymbol + " " + copyrightSymbol
                + " " + copyrightSymbol);
        }
    }

